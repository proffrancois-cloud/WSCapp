function point(x: number, y: number): CampusPoint {
  return { x: Math.round(x * DATA_SCALE), y: Math.round(y * DATA_SCALE) };
}

function runtimePoint(x: number, y: number): CampusPoint {
  return { x: Math.round(x), y: Math.round(y) };
}

function placement(
  id: string,
  asset: EnvironmentAssetKey,
  x: number,
  y: number,
  options: Omit<EnvironmentAssetPlacement, "id" | "asset" | "point"> = {}
): EnvironmentAssetPlacement {
  return {
    id,
    asset,
    point: point(x, y),
    ...options
  };
}

function runtimePlacement(
  id: string,
  asset: EnvironmentAssetKey,
  x: number,
  y: number,
  options: Omit<EnvironmentAssetPlacement, "id" | "asset" | "point"> = {}
): EnvironmentAssetPlacement {
  return {
    id,
    asset,
    point: runtimePoint(x, y),
    ...options
  };
}

function tableCluster(
  prefix: string,
  x: number,
  y: number,
  options: {
    tableAsset?: EnvironmentAssetKey;
    chairAsset?: EnvironmentAssetKey;
    tableScale?: EnvironmentAssetScale;
    chairScale?: EnvironmentAssetScale;
    chairGapX?: number;
    chairGapY?: number;
    rotationY?: number;
  } = {}
): EnvironmentAssetPlacement[] {
  const tableAsset = options.tableAsset || "table";
  const chairAsset = options.chairAsset || "chairCushion";
  const chairGapX = options.chairGapX ?? 86;
  const chairGapY = options.chairGapY ?? 70;

  return [
    placement(`${prefix}-table`, tableAsset, x, y, { rotationY: options.rotationY, scale: options.tableScale ?? 1.25 }),
    placement(`${prefix}-chair-nw`, chairAsset, x - chairGapX, y - chairGapY, { rotationY: Math.PI, scale: options.chairScale ?? 0.92 }),
    placement(`${prefix}-chair-ne`, chairAsset, x + chairGapX, y - chairGapY, { rotationY: Math.PI, scale: options.chairScale ?? 0.92 }),
    placement(`${prefix}-chair-sw`, chairAsset, x - chairGapX, y + chairGapY, { scale: options.chairScale ?? 0.92 }),
    placement(`${prefix}-chair-se`, chairAsset, x + chairGapX, y + chairGapY, { scale: options.chairScale ?? 0.92 })
  ];
}

