function getRuntimeAlpacards(): RuntimeAlpacard[] {
  if (typeof window === "undefined" || !Array.isArray(window.WSC_ALPACARDS)) {
    return [];
  }

  return window.WSC_ALPACARDS;
}

function toCampusAssetUrl(path: string | undefined): string {
  const value = String(path || "").trim();
  if (!value) {
    return "";
  }

  if (/^(https?:|data:|blob:|file:)/i.test(value) || value.startsWith("../") || value.startsWith("./")) {
    return value;
  }

  return `../${value.replace(/^\/+/, "")}`;
}

function splitFrameRow(count: number): { lower: number; upper: number } {
  return {
    lower: Math.ceil(count / 2),
    upper: Math.floor(count / 2)
  };
}

function spacedValues(start: number, end: number, count: number, stagger = 0): number[] {
  if (count <= 0) {
    return [];
  }

  if (count === 1) {
    return [(start + end) / 2];
  }

  const gap = (end - start) / (count - 1);
  return Array.from({ length: count }, (_item, index) => {
    const value = start + gap * index + stagger;
    return Math.min(end, Math.max(start, value));
  });
}

function splitByRangeLength(count: number, ranges: Array<{ start: number; end: number }>): number[] {
  const totalLength = ranges.reduce((sum, range) => sum + Math.max(0, range.end - range.start), 0);
  const roughCounts = ranges.map((range) => ({
    count: Math.floor(count * Math.max(0, range.end - range.start) / totalLength),
    remainder: (count * Math.max(0, range.end - range.start) / totalLength) % 1
  }));
  let assigned = roughCounts.reduce((sum, entry) => sum + entry.count, 0);

  while (assigned < count) {
    const nextIndex = roughCounts
      .map((entry, index) => ({ ...entry, index }))
      .sort((left, right) => right.remainder - left.remainder)[0]?.index ?? 0;
    roughCounts[nextIndex].count += 1;
    roughCounts[nextIndex].remainder = 0;
    assigned += 1;
  }

  return roughCounts.map((entry) => entry.count);
}

function distributeAcrossRanges(count: number, ranges: Array<{ start: number; end: number }>, stagger = 0): number[] {
  const counts = splitByRangeLength(count, ranges);
  return ranges.flatMap((range, index) => spacedValues(range.start, range.end, counts[index], stagger));
}

function getAlpacardImageSrc(contentId: number): string {
  return toCampusAssetUrl(getRuntimeAlpacards()[contentId]?.imagePath);
}

function libraryWallFrame(
  id: string,
  pointValue: CampusPoint,
  wall: CampusWallFramePlacement["wall"],
  bottomHeight: number,
  contentId: number
): CampusWallFramePlacement {
  return {
    id,
    roomId: "guiding-library-lounge",
    itemId: id,
    point: pointValue,
    wall,
    width: LIBRARY_WALL_FRAME_WIDTH,
    height: LIBRARY_WALL_FRAME_HEIGHT,
    bottomHeight,
    contentId,
    imageSrc: getAlpacardImageSrc(contentId)
  };
}

function buildLibraryWallFramePlacements(): CampusWallFramePlacement[] {
  const cards = getRuntimeAlpacards();
  const totalCount = cards.length || LIBRARY_WALL_FRAME_FALLBACK_COUNT;
  const baseWallCount = Math.floor(totalCount / 3);
  const remainder = totalCount % 3;
  const northCount = baseWallCount + (remainder > 0 ? 1 : 0);
  const westCount = baseWallCount + (remainder > 1 ? 1 : 0);
  const eastCount = Math.max(0, totalCount - northCount - westCount);
  const northRows = splitFrameRow(northCount);
  const westRows = splitFrameRow(westCount);
  const eastRows = splitFrameRow(eastCount);
  const northRanges = [{ start: 165, end: 1760 }, { start: 2310, end: 3300 }];
  const sideRange = { start: 270, end: 1970 };
  let contentId = 0;

  const make = (wall: CampusWallFramePlacement["wall"], x: number, y: number, row: "lower" | "upper") => {
    const id = `library-alpacard-frame-${contentId + 1}`;
    const frame = libraryWallFrame(id, runtimePoint(x, y), wall, LIBRARY_WALL_FRAME_BOTTOM_ROWS[row], contentId);
    contentId += 1;
    return frame;
  };

  return [
    ...distributeAcrossRanges(northRows.lower, northRanges).map((x) => make("north", x, 18, "lower")),
    ...distributeAcrossRanges(northRows.upper, northRanges, 42).map((x) => make("north", x, 18, "upper")),
    ...spacedValues(sideRange.start, sideRange.end, westRows.lower).map((y) => make("west", 18, y, "lower")),
    ...spacedValues(sideRange.start, sideRange.end, westRows.upper, 64).map((y) => make("west", 18, y, "upper")),
    ...spacedValues(sideRange.start, sideRange.end, eastRows.lower).map((y) => make("east", 3447, y, "lower")),
    ...spacedValues(sideRange.start, sideRange.end, eastRows.upper, 64).map((y) => make("east", 3447, y, "upper"))
  ].slice(0, totalCount);
}

